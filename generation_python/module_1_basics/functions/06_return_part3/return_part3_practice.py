"=== –ó–∞–¥–∞—á–∞ 3: –ö–æ—Ä–Ω–∏ —É—Ä–∞–≤–Ω–µ–Ω–∏—è üå∂Ô∏è"
# –æ–±—ä—è–≤–ª–µ–Ω–∏–µ —Ñ—É–Ω–∫—Ü–∏–∏
def solve(a, b, c):
    D = (b**2) - (4 * a * c)    # –í—ã—á–∏—Å–ª—è–µ–º –¥–∏—Å–∫—Ä–∏–º–∏–Ω–∞–Ω—Ç (D<0 - –Ω–µ—Ç –∫–æ—Ä–Ω–µ–π, D=0 - 1 –∫–æ—Ä–µ–Ω—å, D>0 - 2–∫–æ—Ä–Ω—è)   
                                # –ø–æ —É—Å–ª–æ–≤–∏—é –∑–∞–¥–∞—á–∏ –≥–∞—Ä–∞–Ω—Ç–∏—Ä—É–µ—Ç—Å—è 1 –∫–æ—Ä–µ–Ω—å. –ï—Å–ª–∏ –∫–æ—Ä–µ–Ω—å 1 –≤—ã–≤–æ–¥–∏–º –µ–≥–æ 2 —Ä–∞–∑–∞

    x = (-b - D ** 0.5) / (2 * a)       # —Ñ–æ—Ä–º—É–ª–∞ –¥–µ–π—Å—Ç–≤–∏—Ç–µ–ª—å–Ω–æ–≥–æ –∫–æ—Ä–Ω—è
    y = (-b + D ** 0.5) / (2 * a)       # —Ñ–æ—Ä–º—É–ª–∞ –¥–µ–π—Å—Ç–≤–∏—Ç–µ–ª—å–Ω–æ–≥–æ –∫–æ—Ä–Ω—è
    return min(x, y), max(x, y)
        
      

