    teens = ["–æ–¥–∏–Ω–Ω–∞–¥—Ü–∞—Ç—å", "–¥–≤–µ–Ω–∞–¥—Ü–∞—Ç—å", "—Ç—Ä–∏–Ω–∞–¥—Ü–∞—Ç—å", "—á–µ—Ç—ã—Ä–Ω–∞–¥—Ü–∞—Ç—å", 
         "–ø—è—Ç–Ω–∞–¥—Ü–∞—Ç—å", "—à–µ—Å—Ç–Ω–∞–¥—Ü–∞—Ç—å", "—Å–µ–º–Ω–∞–¥—Ü–∞—Ç—å", "–≤–æ—Å–µ–º–Ω–∞–¥—Ü–∞—Ç—å", 
         "–¥–µ–≤—è—Ç–Ω–∞–¥—Ü–∞—Ç—å"]
    
    tens = ["–¥–µ—Å—è—Ç—å", "–¥–≤–∞–¥—Ü–∞—Ç—å", "—Ç—Ä–∏–¥—Ü–∞—Ç—å", "—Å–æ—Ä–æ–∫", "–ø—è—Ç—å–¥–µ—Å—è—Ç", 
        "—à–µ—Å—Ç—å–¥–µ—Å—è—Ç", "—Å–µ–º—å–¥–µ—Å—è—Ç", "–≤–æ—Å–µ–º—å–¥–µ—Å—è—Ç", "–¥–µ–≤—è–Ω–æ—Å—Ç–æ"]
    
    if 1 <= num <= 9:
        return units[num - 1] 
    elif 11 <= num <= 19:
        return teens[num - 11]
    else:
        tens_digit = num // 10              # –Ω–∞—Ö–æ–¥–∏–º –¥–µ—Å—è—Ç–∫–∏ - 44 // 10 = 4 (—Å–æ—Ä–æ–∫)
