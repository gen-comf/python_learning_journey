"=== –ó–∞–¥–∞—á–∞ 7: –ü–∞–Ω–≥—Ä–∞–º–º—ã üå∂Ô∏è"
def is_pangram(text):
    alphabet = 'abcdefghijklmnopqrstuvwxyz'     # –ê–ª—Ñ–∞–≤–∏—Ç –∫–∞–∫ —Å—Ç—Ä–æ–∫–∞ –¥–ª—è –±—ã—Å—Ç—Ä–æ–≥–æ –ø–æ–∏—Å–∫–∞
    found = [False] * 26                        # –°–ø–∏—Å–æ–∫ –¥–ª—è –æ—Ç—Å–ª–µ–∂–∏–≤–∞–Ω–∏—è –Ω–∞–π–¥–µ–Ω–Ω—ã—Ö –±—É–∫–≤

    for char in text.lower():
        if char != ' ':                         # –ü—Ä–æ–ø—É—Å–∫–∞–µ–º –ø—Ä–æ–±–µ–ª—ã
            if char in alphabet:
                index = alphabet.index(char)    #–ù–∞—Ö–æ–¥–∏–º –∏–Ω–¥–µ–∫—Å –±—É–∫–≤—ã –≤ –∞–ª—Ñ–∞–≤–∏—Ç–µ
                found[index] = True

    return all(found)                           # –ü—Ä–æ–≤–µ—Ä—è–µ–º, –≤—Å–µ –ª–∏ True –≤ —Å–ø–∏—Å–∫–µ found


