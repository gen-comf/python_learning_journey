        word =  tens[tens_digit - 1]        # word = —Å–æ—Ä–æ–∫ (—É–∂–µ –µ—Å—Ç—å)
    
        if ones_digit > 0:
            word += ' ' + units[ones_digit - 1]     # –≤ –¥–∞–Ω–Ω–æ–º —Å–ª—É—á–∞–µ –¥–æ–±–∞–≤–ª—è–µ–º –∫ —Å–æ—Ä–æ–∫ (—á–µ—Ç—ã—Ä–µ)

        return word                                 # –≤—ã–≤–æ–¥–∏–º —Ä–µ–∑—É–ª—å—Ç–∞—Ç


