# ===== ЗАДАЧА 7 Три слова=====
"""
На вход программе подается строка, состоящая из трех слов. Верно ли, что для записи всех трех слов был использован один и тот же набор букв?
Формат входных данных
На вход программе подается строка, состоящая из трех слов.
Формат выходных данных
Программа должна вывести YES, если для записи всех трех слов был использован один и тот же набор букв, или NO в противном случае.
"""
# Получаем строку и разбиваем на слова
words = input().split()

# Преобразуем каждое слово в множество букв
sets = [set(word) for word in words]

# Проверяем, равны ли все множества
if sets[0] == sets[1] == sets[2]:
    print("YES")
else:
    print("NO")
