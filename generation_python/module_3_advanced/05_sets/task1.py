# ===== ЗАДАЧА 1 Операции с множествами 1=====
"""
Вам доступно множество numbers. Напишите программу, которая выводит сумму квадратов элементов множества numbers.
Примечание. Считайте, что множество numbers уже объявлен в вашей программе, и вы имеете к нему доступ.
"""
numbers = {10, 2.5, 3, 42, 51, 10, -96}
square_sum = 0
for el in numbers:
    square_sum += el ** 2
    
print(square_sum)

# ===== ЗАДАЧА 2 Операции с множествами 2=====
"""
Вам доступно множество fruits. Напишите программу, которая выводит элементы множества fruits, 
каждый на отдельной строке, отсортированные по убыванию (в обратном лексикографическом порядке).
Примечание 1. Считайте, что множество fruits уже объявлено в вашей программе, и вы имеете к нему доступ.
Примечание 2. Выводите каждый элемент множества на отдельной строке.
"""
