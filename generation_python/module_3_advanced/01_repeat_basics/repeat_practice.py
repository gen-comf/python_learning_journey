# ===== ЗАДАЧА 7 Standard American Convention =====
"""
Условие: 
На вход программе подается натуральное число. Напишите программу, которая вставляет в заданное число запятые в соответствии 
со стандартным американским соглашением о запятых в больших числах.
к примеру 1000000 на выходе 1,000,000
"""

def standard_amer_convection(num):
    num = str(num)
    # создаем список для результат
    result = []

    #переворачиваем строку и добавляя в result каждый символ digit
    for i, digit in enumerate(reversed(num), 1):
        result.append(digit)
        #проверяем индекс символа на кратность 3м и дополнительная проверка на i != len(num), которая позволит не добавлять ',' перед 1 цифрой заданного числа
        if i % 3 == 0 and i != len(num):
            result.append(',')

    #возвращаем результат
    return ''.join(reversed(result))
