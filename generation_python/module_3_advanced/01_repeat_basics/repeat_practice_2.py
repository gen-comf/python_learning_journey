# ===== ЗАДАЧА 9  Орел и решка ↔=====
"""
Дана строка текста, состоящая из букв русского алфавита "О" и "Р". Буква "О" соответствует выпадению Орла, а буква "Р" – выпадению Решки. 
Напишите программу, которая подсчитывает наибольшее количество подряд выпавших Решек.
Формат входных данных
На вход программе подается строка текста, состоящая из букв русского алфавита "О" и "Р".
Формат выходных данных
Программа должна вывести наибольшее количество подряд выпавших Решек.
Примечание. Если выпавших Решек нет, то необходимо вывести число 0.
"""

s = input().strip()

max_count = 0               # макс количество "Р" подряд
current_count = 0           # текущее количество "Р" подряд

for char in s:
  if char =="Р":
    current_count += 1
    if current_count > max_count:
      max_count = current_count
  else:
    current_count = 0               # сброс счетчика, если выпадает "О"
   
print(max_count)

# ===== ЗАДАЧА 10  Кремниевая долина 🤖🌶️ ↔=====
"""
Искусственный интеллект Антон, созданный Гилфойлом, взломал сеть умных холодильников. Теперь он использует их в качестве серверов "Пегого дудочника". 
Помогите владельцу фирмы отыскать все зараженные холодильники.
Для каждого холодильника существует строка с данными, состоящая из строчных букв и цифр, и если в ней присутствует слово "anton" 
(необязательно рядом стоящие буквы, главное наличие последовательности букв), то холодильник заражен и нужно вывести номер холодильника, 
нумерация начинается с единицы.
Формат входных данных
В первой строке подается число n – количество холодильников. В последующих n строках вводятся строки, 
содержащие латинские строчные буквы и цифры, в каждой строке от 5 до 100 символов.
Формат выходных данных
Программа должна вывести номера зараженных холодильников через пробел. Если таких холодильников нет, ничего выводить не нужно.
"""
n = int(input())
infected = []               # создаем пустой лист в котором будут № зараженных холодильников
target = "anton"

for num in range(1, n + 1):
  text = input().strip()
  stage = 0                     # счет шагов
  
  for el in text:
    if el == target[stage]:         # в первом прогоне ищем букву "a"
      stage += 1
      if stage == len(target):       # если количеств ошагов совпадет с длиной target
        infected.append(num)
        break


print(*infected) 