# ===== ЗАДАЧА 6 Произведение чисел  ↔=====
"""
Напишите программу для определения, является ли число произведением двух чисел из данного набора. 
Программа должна выводить результат в виде ответа ДА или НЕТ.
Формат входных данных
В первой строке подается натуральное число n(1<n<1000) – количество чисел в наборе. В последующих 
n строках вводятся целые числа, составляющие набор (могут повторяться). Затем следует целое число, 
которое является или не является произведением двух каких-то чисел из набора.
"""
n = int(input())
numbers = [int(input()) for _ in range(n)]
x = int(input())

found = False

for i in range(n - 1):           # последний элемент проверяем только как второй множитель
    for j in range(i + 1, n):    # начинаем со следующего элемента
        if numbers[i] * numbers[j] == x:
            found = True
            break
    if found:
        break

print("ДА" if found else "НЕТ")

