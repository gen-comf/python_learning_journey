# ===== ЗАДАЧА 1 Координатные четверти=====
"""
Условие: 
Дан набор точек на координатной плоскости. Необходимо подсчитать и вывести количество точек, лежащих в каждой координатной четверти.
Формат входных данных
В первой строке записано количество точек. Каждая следующая строка состоит из двух целых чисел – координат точки (сначала абсцисса – 
x, затем ордината – y), разделенных символом пробела.
"""
n = int(input())
count = [0, 0, 0, 0]
names = ['Первая четверть:', 'Вторая четверть:', 
         'Третья четверть:', 'Четвертая четверть:']

for _ in range(n):
    x, y = [int(num) for num in input().split()]
    if x > 0 and y > 0:
        count[0] += 1
    elif x < 0 and y > 0:
        count[1] += 1
    elif x < 0 and y < 0:
        count[2] += 1
    elif x > 0 and y < 0:
        count[3] += 1
        
for i in range(4):
    print(names[i], count[i])


# ===== ЗАДАЧА 2 Больше предыдущего=====
"""
Условие: 
На вход программе подается строка текста из натуральных чисел. Из нее формируется список чисел. Напишите программу подсчета количества чисел, которые больше предшествующего им в этом списке числа, то есть, стоят вслед за меньшим числом. 
Формат входных данных
На вход программе подается строка текста из разделенных пробелами натуральных чисел.
"""

digits = [int(num) for num in input().split()]
count = 0

for i in range(1, len(digits)):
    if digits[i] > digits[i - 1]:
        count += 1
            
print(count)


# ===== ЗАДАЧА 3 Назад, вперед и наоборот ↔️=====
"""
На вход программе подается строка текста из неотрицательных чисел. Из элементов строки формируется список чисел. 
Напишите программу, которая меняет местами соседние элементы списка (a[0] c a[1], a[2] c a[3] и так далее). 
Если в списке нечетное количество элементов, то последний остается на своем месте.
Формат входных данных
На вход программе подается строка текста, содержащая неотрицательные числа, разделенные пробелами.
"""
numbers = input().split()

for i in range(0, len(numbers) - 1, 2):
    numbers[i], numbers[i + 1] = numbers[i + 1], numbers[i]
    
print(*numbers)

# ===== ЗАДАЧА 4  Сдвиг в развитии ↩️ ↔=====
"""
На вход программе подается строка текста из натуральных чисел. Из элементов строки формируется список чисел.
Напишите программу циклического сдвига элементов списка направо, когда последний элемент становится первым, 
а остальные сдвигаются на одну позицию вперед, в сторону увеличения индексов.
Формат входных данных
На вход программе подается строка текста из разделенных пробелами натуральных чисел.
"""
my_list = input().split()
# сдвиг вправо
shifted = my_list[-1:] + my_list[:-1]   # my_list[-1:] последний элемент как список + my_list[:-1] все элементы за искл последнего
# сдвиг влево
shifted_l = my_list[1:] + my_list[:1]
print(*shifted)
print(*shifted_l)

# ===== ЗАДАЧА 5  Различные элементы  ↔=====
"""
На вход программе подается строка текста, содержащая натуральные числа, расположенные по неубыванию. 
Из строки формируется список чисел. Напишите программу для подсчета количества разных элементов в списке.
Формат входных данных
На вход программе подается строка текста, содержащая натуральные числа, разделенные символом пробела, расположенные по неубыванию.
"""
my_list = [int(i) for i in input().split()]
result = []

for i in my_list:
    if not i in result:         #добавляем только уникальные числа
        result.append(i)
        

print(len(result))              #длина result[] даст ответ


# ===== ЗАДАЧА 6 Произведение чисел  ↔=====
"""
Напишите программу для определения, является ли число произведением двух чисел из данного набора. 
Программа должна выводить результат в виде ответа ДА или НЕТ.
Формат входных данных
В первой строке подается натуральное число n(1<n<1000) – количество чисел в наборе. В последующих 
n строках вводятся целые числа, составляющие набор (могут повторяться). Затем следует целое число, 
которое является или не является произведением двух каких-то чисел из набора.
"""
n = int(input())
numbers = [int(input()) for _ in range(n)]
x = int(input())

found = False

for i in range(n - 1):           # последний элемент проверяем только как второй множитель
    for j in range(i + 1, n):    # начинаем со следующего элемента
        if numbers[i] * numbers[j] == x:
            found = True
            break
    if found:
        break

print("ДА" if found else "НЕТ")