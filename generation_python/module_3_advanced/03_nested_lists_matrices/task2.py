# ===== –ó–ê–î–ê–ß–ê 6 –ú–∞–∫—Å–∏–º–∞–ª—å–Ω—ã–π –≤ –æ–±–ª–∞—Å—Ç–∏ 2 üå∂Ô∏è =====
"""
–ù–∞–ø–∏—à–∏—Ç–µ –ø—Ä–æ–≥—Ä–∞–º–º—É, –∫–æ—Ç–æ—Ä–∞—è –≤—ã–≤–æ–¥–∏—Ç –º–∞–∫—Å–∏–º–∞–ª—å–Ω—ã–π —ç–ª–µ–º–µ–Ω—Ç –≤ –∑–∞—à—Ç—Ä–∏—Ö–æ–≤–∞–Ω–Ω–æ–π –æ–±–ª–∞—Å—Ç–∏ –∫–≤–∞–¥—Ä–∞—Ç–Ω–æ–π –º–∞—Ç—Ä–∏—Ü—ã.
–§–æ—Ä–º–∞—Ç –≤—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
–ù–∞ –≤—Ö–æ–¥ –ø—Ä–æ–≥—Ä–∞–º–º–µ –ø–æ–¥–∞–µ—Ç—Å—è –Ω–∞—Ç—É—Ä–∞–ª—å–Ω–æ–µ —á–∏—Å–ª–æ n ‚Äì –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —Å—Ç—Ä–æ–∫ –∏ —Å—Ç–æ–ª–±—Ü–æ–≤ –≤ –º–∞—Ç—Ä–∏—Ü–µ, –∑–∞—Ç–µ–º —ç–ª–µ–º–µ–Ω—Ç—ã –º–∞—Ç—Ä–∏—Ü—ã (—Ü–µ–ª—ã–µ —á–∏—Å–ª–∞) –ø–æ—Å—Ç—Ä–æ—á–Ω–æ —á–µ—Ä–µ–∑ –ø—Ä–æ–±–µ–ª.
"""
n = int(input())
matrix = [list(map(int, input().split())) for _ in range(n)]

max_left = matrix[0][0]      # –î–ª—è –ª–µ–≤–æ–≥–æ —Ç—Ä–µ—É–≥–æ–ª—å–Ω–∏–∫–∞
max_right = matrix[0][n-1]   # –î–ª—è –ø—Ä–∞–≤–æ–≥–æ —Ç—Ä–µ—É–≥–æ–ª—å–Ω–∏–∫–∞

for i in range(n):
    for j in range(n):
        # –õ–µ–≤—ã–π —Ç—Ä–µ—É–≥–æ–ª—å–Ω–∏–∫
        if j <= i and i + j <= n - 1:
            if matrix[i][j] > max_left:
                max_left = matrix[i][j]
        
        # –ü—Ä–∞–≤—ã–π —Ç—Ä–µ—É–≥–æ–ª—å–Ω–∏–∫  
        if j >= i and i + j >= n - 1:
            if matrix[i][j] > max_right:
                max_right = matrix[i][j]

# –û–±—â–∏–π –º–∞–∫—Å–∏–º—É–º –∏–∑ –¥–≤—É—Ö —Ç—Ä–µ—É–≥–æ–ª—å–Ω–∏–∫–æ–≤
print(max(max_left, max_right))     

# –ì–µ–Ω–µ—Ä–∞—Ç–æ—Ä—ã –¥–ª—è –æ–±–æ–∏—Ö —Ç—Ä–µ—É–≥–æ–ª—å–Ω–∏–∫–æ–≤
max_left = max(matrix[i][j] for i in range(n) for j in range(i+1) if i+j <= n-1)
max_right = max(matrix[i][j] for i in range(n) for j in range(i, n) if i+j >= n-1)

print(max(max_left, max_right)) 


