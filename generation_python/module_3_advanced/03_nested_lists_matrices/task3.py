# ===== –ó–ê–î–ê–ß–ê 1 –¢–∞–±–ª–∏—Ü–∞ —É–º–Ω–æ–∂–µ–Ω–∏—è=====
"""
–ù–∞ –≤—Ö–æ–¥ –ø—Ä–æ–≥—Ä–∞–º–º–µ –ø–æ–¥–∞—é—Ç—Å—è –¥–≤–∞ –Ω–∞—Ç—É—Ä–∞–ª—å–Ω—ã—Ö —á–∏—Å–ª–∞ n –∏ m ‚Äì –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —Å—Ç—Ä–æ–∫ –∏ —Å—Ç–æ–ª–±—Ü–æ–≤ –≤ –º–∞—Ç—Ä–∏—Ü–µ. 
–°–æ–∑–¥–∞–π—Ç–µ –º–∞—Ç—Ä–∏—Ü—É mult —Ä–∞–∑–º–µ—Ä–æ–º n√óm –∏ –∑–∞–ø–æ–ª–Ω–∏—Ç–µ –µ–µ —Ç–∞–±–ª–∏—Ü–µ–π —É–º–Ω–æ–∂–µ–Ω–∏—è –ø–æ —Ñ–æ—Ä–º—É–ª–µ mult[i][j] = i * j
–§–æ—Ä–º–∞—Ç –≤—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
–ù–∞ –≤—Ö–æ–¥ –ø—Ä–æ–≥—Ä–∞–º–º–µ –Ω–∞ —Ä–∞–∑–Ω—ã—Ö —Å—Ç—Ä–æ–∫–∞—Ö –ø–æ–¥–∞—é—Ç—Å—è –¥–≤–∞ —á–∏—Å–ª–∞ n –∏ m ‚Äì –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —Å—Ç—Ä–æ–∫ –∏ —Å—Ç–æ–ª–±—Ü–æ–≤ –≤ –º–∞—Ç—Ä–∏—Ü–µ.
"""
n, m = int(input()), int(input())
my_list = [[row * col for col in range(m)] for row in range(n)]

 
for r in range(n):
    for c in range(m):
        print(str(my_list[r][c]).ljust(3), end="")
    print()
    
# ===== –ó–ê–î–ê–ß–ê 2 –ú–∞–∫—Å–∏–º—É–º –≤ —Ç–∞–±–ª–∏—Ü–µ üîù=====
"""
–ù–∞ –≤—Ö–æ–¥ –ø—Ä–æ–≥—Ä–∞–º–º–µ –ø–æ–¥–∞—é—Ç—Å—è –¥–≤–∞ –Ω–∞—Ç—É—Ä–∞–ª—å–Ω—ã—Ö —á–∏—Å–ª–∞ n –∏ m ‚Äì –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —Å—Ç—Ä–æ–∫ –∏ —Å—Ç–æ–ª–±—Ü–æ–≤ –≤ –º–∞—Ç—Ä–∏—Ü–µ, –∑–∞—Ç–µ–º 
n —Å—Ç—Ä–æ–∫ –ø–æ m —Ü–µ–ª—ã—Ö —á–∏—Å–µ–ª –≤ –∫–∞–∂–¥–æ–π, –æ—Ç–¥–µ–ª–µ–Ω–Ω—ã—Ö —Å–∏–º–≤–æ–ª–æ–º –ø—Ä–æ–±–µ–ª–∞.
–ù–∞–ø–∏—à–∏—Ç–µ –ø—Ä–æ–≥—Ä–∞–º–º—É, –∫–æ—Ç–æ—Ä–∞—è –Ω–∞—Ö–æ–¥–∏—Ç –∏–Ω–¥–µ–∫—Å—ã (—Å—Ç—Ä–æ–∫—É –∏ —Å—Ç–æ–ª–±–µ—Ü) –ø–µ—Ä–≤–æ–≥–æ –≤—Ö–æ–∂–¥–µ–Ω–∏—è –º–∞–∫—Å–∏–º–∞–ª—å–Ω–æ–≥–æ —ç–ª–µ–º–µ–Ω—Ç–∞.
–§–æ—Ä–º–∞—Ç –≤—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
–ù–∞ –≤—Ö–æ–¥ –ø—Ä–æ–≥—Ä–∞–º–º–µ –Ω–∞ —Ä–∞–∑–Ω—ã—Ö —Å—Ç—Ä–æ–∫–∞—Ö –ø–æ–¥–∞—é—Ç—Å—è –¥–≤–∞ —á–∏—Å–ª–∞ n –∏ m ‚Äì –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —Å—Ç—Ä–æ–∫ –∏ —Å—Ç–æ–ª–±—Ü–æ–≤ –≤ –º–∞—Ç—Ä–∏—Ü–µ, –∑–∞—Ç–µ–º —Å–∞–º–∏ —ç–ª–µ–º–µ–Ω—Ç—ã –º–∞—Ç—Ä–∏—Ü—ã –ø–æ—Å—Ç—Ä–æ—á–Ω–æ —á–µ—Ä–µ–∑ –ø—Ä–æ–±–µ–ª.
–§–æ—Ä–º–∞—Ç –≤—ã—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
–ü—Ä–æ–≥—Ä–∞–º–º–∞ –¥–æ–ª–∂–Ω–∞ –≤—ã–≤–µ—Å—Ç–∏ –¥–≤–∞ —á–∏—Å–ª–∞: –Ω–æ–º–µ—Ä —Å—Ç—Ä–æ–∫–∏ –∏ –Ω–æ–º–µ—Ä —Å—Ç–æ–ª–±—Ü–∞, –≤ –∫–æ—Ç–æ—Ä—ã—Ö —Å—Ç–æ–∏—Ç –Ω–∞–∏–±–æ–ª—å—à–∏–π —ç–ª–µ–º–µ–Ω—Ç —Ç–∞–±–ª–∏—Ü—ã. 
–ï—Å–ª–∏ —Ç–∞–∫–∏—Ö —ç–ª–µ–º–µ–Ω—Ç–æ–≤ –Ω–µ—Å–∫–æ–ª—å–∫–æ, —Ç–æ –≤—ã–≤–æ–¥–∏—Ç—Å—è —Ç–æ—Ç, —É –∫–æ—Ç–æ—Ä–æ–≥–æ –º–µ–Ω—å—à–µ –Ω–æ–º–µ—Ä —Å—Ç—Ä–æ–∫–∏, –∞ –µ—Å–ª–∏ –Ω–æ–º–µ—Ä–∞ —Å—Ç—Ä–æ–∫ —Ä–∞–≤–Ω—ã, —Ç–æ —Ç–æ—Ç, —É –∫–æ—Ç–æ—Ä–æ–≥–æ –º–µ–Ω—å—à–µ –Ω–æ–º–µ—Ä —Å—Ç–æ–ª–±—Ü–∞
"""
n, m = int(input()), int(input())

matrix = [list(map(int, input().split())) for _ in range(n)]


max_value = matrix[0][0]
max_i, max_j = 0, 0  # –ó–∞–ø–æ–º–∏–Ω–∞–µ–º –∫–æ–æ—Ä–¥–∏–Ω–∞—Ç—ã –ø–µ—Ä–≤–æ–≥–æ –º–∞–∫—Å–∏–º—É–º–∞

for i in range(n):
    for j in range(m):
        if matrix[i][j] > max_value:
            max_value = matrix[i][j]
            max_i, max_j = i, j

print(max_i, max_j)

# ===== –ó–ê–î–ê–ß–ê 3 –û–±–º–µ–Ω —Å—Ç–æ–ª–±—Ü–æ–≤ ‚è∏Ô∏è=====
"""
–ù–∞–ø–∏—à–∏—Ç–µ –ø—Ä–æ–≥—Ä–∞–º–º—É, –∫–æ—Ç–æ—Ä–∞—è –º–µ–Ω—è–µ—Ç –º–µ—Å—Ç–∞–º–∏ —Å—Ç–æ–ª–±—Ü—ã –≤ –º–∞—Ç—Ä–∏—Ü–µ.
–ù–∞ –≤—Ö–æ–¥ –ø—Ä–æ–≥—Ä–∞–º–º–µ –Ω–∞ —Ä–∞–∑–Ω—ã—Ö —Å—Ç—Ä–æ–∫–∞—Ö –ø–æ–¥–∞—é—Ç—Å—è –¥–≤–∞ –Ω–∞—Ç—É—Ä–∞–ª—å–Ω—ã—Ö —á–∏—Å–ª–∞ n –∏ m
‚Äì –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —Å—Ç—Ä–æ–∫ –∏ —Å—Ç–æ–ª–±—Ü–æ–≤ –≤ –º–∞—Ç—Ä–∏—Ü–µ, –∑–∞—Ç–µ–º —ç–ª–µ–º–µ–Ω—Ç—ã –º–∞—Ç—Ä–∏—Ü—ã –ø–æ—Å—Ç—Ä–æ—á–Ω–æ —á–µ—Ä–µ–∑ –ø—Ä–æ–±–µ–ª, –∑–∞—Ç–µ–º –Ω–∞—Ç—É—Ä–∞–ª—å–Ω—ã–µ —á–∏—Å–ª–∞ 
i –∏ j ‚Äì –Ω–æ–º–µ—Ä–∞ —Å—Ç–æ–ª–±—Ü–æ–≤, –ø–æ–¥–ª–µ–∂–∞—â–∏—Ö –æ–±–º–µ–Ω—É.
"""
n, m = int(input()), int(input())

matrix = [list(map(int, input().split())) for _ in range(n)]

i, j = map(int, input().split())

for row in matrix:
    row[i], row[j] = row[j], row[i]

for r in range(n):
    for c in range(m):
        print(str(matrix[r][c]), end=" ")
    print()

# ===== –ó–ê–î–ê–ß–ê 4 –°–∏–º–º–µ—Ç—Ä–∏—á–Ω–∞—è –º–∞—Ç—Ä–∏—Ü–∞ üí†=====
"""
–ù–∞–ø–∏—à–∏—Ç–µ –ø—Ä–æ–≥—Ä–∞–º–º—É, –∫–æ—Ç–æ—Ä–∞—è –ø—Ä–æ–≤–µ—Ä—è–µ—Ç —Å–∏–º–º–µ—Ç—Ä–∏—á–Ω–æ—Å—Ç—å –∫–≤–∞–¥—Ä–∞—Ç–Ω–æ–π –º–∞—Ç—Ä–∏—Ü—ã –æ—Ç–Ω–æ—Å–∏—Ç–µ–ª—å–Ω–æ –≥–ª–∞–≤–Ω–æ–π –¥–∏–∞–≥–æ–Ω–∞–ª–∏.
–§–æ—Ä–º–∞—Ç –≤—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
–ù–∞ –≤—Ö–æ–¥ –ø—Ä–æ–≥—Ä–∞–º–º–µ –ø–æ–¥–∞–µ—Ç—Å—è –Ω–∞—Ç—É—Ä–∞–ª—å–Ω–æ–µ —á–∏—Å–ª–æ n ‚Äì –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —Å—Ç—Ä–æ–∫ –∏ —Å—Ç–æ–ª–±—Ü–æ–≤ –≤ –º–∞—Ç—Ä–∏—Ü–µ, –∑–∞—Ç–µ–º —ç–ª–µ–º–µ–Ω—Ç—ã –º–∞—Ç—Ä–∏—Ü—ã –ø–æ—Å—Ç—Ä–æ—á–Ω–æ —á–µ—Ä–µ–∑ –ø—Ä–æ–±–µ–ª.
"""
n = int(input())

matrix = [list(map(int, input().split())) for _ in range(n)]

for i in range(n):
    for j in range(n):
        if matrix[i][j] != matrix[j][i]:
            print("NO")
            break
    else:
        continue        # –ï—Å–ª–∏ –Ω–µ –±—ã–ª–æ break, –ø—Ä–æ–¥–æ–ª–∂–∞–µ–º –≤–Ω–µ—à–Ω–∏–π —Ü–∏–∫–ª
    break               # –ï—Å–ª–∏ –±—ã–ª break –≤–æ –≤–Ω—É—Ç—Ä–µ–Ω–Ω–µ–º —Ü–∏–∫–ª–µ, –≤—ã—Ö–æ–¥–∏–º –∏–∑ –≤–Ω–µ—à–Ω–µ–≥–æ

else:
    print("YES")

# ===== –ó–ê–î–ê–ß–ê 5 –û–±–º–µ–Ω –¥–∏–∞–≥–æ–Ω–∞–ª–µ–π üîÉ=====
"""
–î–∞–Ω–∞ –∫–≤–∞–¥—Ä–∞—Ç–Ω–∞—è –º–∞—Ç—Ä–∏—Ü–∞ —á–∏—Å–µ–ª. –ù–∞–ø–∏—à–∏—Ç–µ –ø—Ä–æ–≥—Ä–∞–º–º—É, –∫–æ—Ç–æ—Ä–∞—è –º–µ–Ω—è–µ—Ç –º–µ—Å—Ç–∞–º–∏ —ç–ª–µ–º–µ–Ω—Ç—ã, 
—Å—Ç–æ—è—â–∏–µ –Ω–∞ –≥–ª–∞–≤–Ω–æ–π –∏ –ø–æ–±–æ—á–Ω–æ–π –¥–∏–∞–≥–æ–Ω–∞–ª–∏, –ø—Ä–∏ —ç—Ç–æ–º –∫–∞–∂–¥—ã–π —ç–ª–µ–º–µ–Ω—Ç –¥–æ–ª–∂–µ–Ω –æ—Å—Ç–∞—Ç—å—Å—è –≤ —Ç–æ–º –∂–µ 
—Å—Ç–æ–ª–±—Ü–µ (—Ç–æ –µ—Å—Ç—å –≤ –∫–∞–∂–¥–æ–º —Å—Ç–æ–ª–±—Ü–µ –Ω—É–∂–Ω–æ –ø–æ–º–µ–Ω—è—Ç—å –º–µ—Å—Ç–∞–º–∏ —ç–ª–µ–º–µ–Ω—Ç –Ω–∞ –≥–ª–∞–≤–Ω–æ–π –¥–∏–∞–≥–æ–Ω–∞–ª–∏ –∏ –Ω–∞ –ø–æ–±–æ—á–Ω–æ–π –¥–∏–∞–≥–æ–Ω–∞–ª–∏).
"""
n = int(input())
matrix = [list(map(int, input().split())) for _ in range(n)]

# –ú–µ–Ω—è–µ–º —ç–ª–µ–º–µ–Ω—Ç—ã –≤ –∫–∞–∂–¥–æ–º —Å—Ç–æ–ª–±—Ü–µ
for j in range(n):
    # j - –Ω–æ–º–µ—Ä —Å—Ç–æ–ª–±—Ü–∞
    # –ì–ª–∞–≤–Ω–∞—è –¥–∏–∞–≥–æ–Ω–∞–ª—å –≤ —Å—Ç–æ–ª–±—Ü–µ j: matrix[j][j]
    # –ü–æ–±–æ—á–Ω–∞—è –¥–∏–∞–≥–æ–Ω–∞–ª—å –≤ —Å—Ç–æ–ª–±—Ü–µ j: matrix[n-1-j][j]
    matrix[j][j], matrix[n-1-j][j] = matrix[n-1-j][j], matrix[j][j] # –æ—Ç–Ω–æ—Å–∏—Ç–µ–ª—å–Ω–æ –æ—Å–∏ x
    # matrix[j][j], matrix[j][n-1-j] = matrix[j][n-1-j], matrix[j][j] # –æ—Ç–Ω–æ—Å–∏—Ç–µ–ª—å–Ω–æ –æ—Å–∏ y 
# –í—ã–≤–æ–¥
for row in matrix:
    print(*row)

# ===== –ó–ê–î–ê–ß–ê 6 –ó–µ—Ä–∫–∞–ª—å–Ω–æ–µ –æ—Ç–æ–±—Ä–∞–∂–µ–Ω–∏–µ ü¶ã=====
"""
–î–∞–Ω–∞ –∫–≤–∞–¥—Ä–∞—Ç–Ω–∞—è –º–∞—Ç—Ä–∏—Ü–∞ —á–∏—Å–µ–ª. –ù–∞–ø–∏—à–∏—Ç–µ –ø—Ä–æ–≥—Ä–∞–º–º—É, –∫–æ—Ç–æ—Ä–∞—è –∑–µ—Ä–∫–∞–ª—å–Ω–æ –æ—Ç–æ–±—Ä–∞–∂–∞–µ—Ç –µ—ë —ç–ª–µ–º–µ–Ω—Ç—ã –æ—Ç–Ω–æ—Å–∏—Ç–µ–ª—å–Ω–æ –≥–æ—Ä–∏–∑–æ–Ω—Ç–∞–ª—å–Ω–æ–π –æ—Å–∏ —Å–∏–º–º–µ—Ç—Ä–∏–∏.
–§–æ—Ä–º–∞—Ç –≤—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö:
–ù–∞ –≤—Ö–æ–¥ –ø—Ä–æ–≥—Ä–∞–º–º–µ –ø–æ–¥–∞–µ—Ç—Å—è –Ω–∞—Ç—É—Ä–∞–ª—å–Ω–æ–µ —á–∏—Å–ª–æ n ‚Äì –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —Å—Ç—Ä–æ–∫ –∏ —Å—Ç–æ–ª–±—Ü–æ–≤ –≤ –º–∞—Ç—Ä–∏—Ü–µ, –∑–∞—Ç–µ–º —ç–ª–µ–º–µ–Ω—Ç—ã –º–∞—Ç—Ä–∏—Ü—ã –ø–æ—Å—Ç—Ä–æ—á–Ω–æ —á–µ—Ä–µ–∑ –ø—Ä–æ–±–µ–ª.
"""
n = int(input())
matrix = [list(map(int, input().split())) for _ in range(n)]

# –ú–µ–Ω—è–µ–º —Å—Ç—Ä–æ–∫–∏ –º–µ—Å—Ç–∞–º–∏ (–±–µ–∑ –ø–æ—ç–ª–µ–º–µ–Ω—Ç–Ω–æ–≥–æ –æ–±–º–µ–Ω–∞)
for i in range(n // 2):
    matrix[i], matrix[n-1-i] = matrix[n-1-i], matrix[i]

for row in matrix:
        print(*row)


# ===== –ó–ê–î–ê–ß–ê 7 –ü–æ–≤–æ—Ä–æ—Ç –º–∞—Ç—Ä–∏—Ü—ã üîÅ=====
"""
–ù–∞–ø–∏—à–∏—Ç–µ –ø—Ä–æ–≥—Ä–∞–º–º—É, –∫–æ—Ç–æ—Ä–∞—è –ø–æ–≤–æ—Ä–∞—á–∏–≤–∞–µ—Ç –∫–≤–∞–¥—Ä–∞—Ç–Ω—É—é –º–∞—Ç—Ä–∏—Ü—É —á–∏—Å–µ–ª –Ω–∞ 90‚àò –ø–æ —á–∞—Å–æ–≤–æ–π —Å—Ç—Ä–µ–ª–∫–µ.
–§–æ—Ä–º–∞—Ç –≤—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö:
–ù–∞ –≤—Ö–æ–¥ –ø—Ä–æ–≥—Ä–∞–º–º–µ –ø–æ–¥–∞–µ—Ç—Å—è –Ω–∞—Ç—É—Ä–∞–ª—å–Ω–æ–µ —á–∏—Å–ª–æ n ‚Äì –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —Å—Ç—Ä–æ–∫ –∏ —Å—Ç–æ–ª–±—Ü–æ–≤ –≤ –º–∞—Ç—Ä–∏—Ü–µ, –∑–∞—Ç–µ–º —ç–ª–µ–º–µ–Ω—Ç—ã –º–∞—Ç—Ä–∏—Ü—ã –ø–æ—Å—Ç—Ä–æ—á–Ω–æ —á–µ—Ä–µ–∑ –ø—Ä–æ–±–µ–ª.
"""
n = int(input())

matrix = [list(map(int, input().split())) for _ in range(n)]

for i in range(n):
    for j in range(n - 1, -1, -1):  # –°—Ç–æ–ª–±—Ü—ã –≤ –æ–±—Ä–∞—Ç–Ω–æ–º –ø–æ—Ä—è–¥–∫–µ
        print(matrix[j][i], end=' ')
    print()

# ===== –ó–ê–î–ê–ß–ê 8 –•–æ–¥—ã –∫–æ–Ω—è üêé=====
"""
–ù–∞ —à–∞—Ö–º–∞—Ç–Ω–æ–π –¥–æ—Å–∫–µ 8√ó8 —Å—Ç–æ–∏—Ç –∫–æ–Ω—å. –ù–∞–ø–∏—à–∏—Ç–µ –ø—Ä–æ–≥—Ä–∞–º–º—É, –∫–æ—Ç–æ—Ä–∞—è –æ—Ç–º–µ—á–∞–µ—Ç –ø–æ–ª–æ–∂–µ–Ω–∏–µ –∫–æ–Ω—è –Ω–∞ –¥–æ—Å–∫–µ –∏ –≤—Å–µ –∫–ª–µ—Ç–∫–∏, –∫–æ—Ç–æ—Ä—ã–µ –±—å–µ—Ç –∫–æ–Ω—å. 
–ö–ª–µ—Ç–∫—É, –≥–¥–µ —Å—Ç–æ–∏—Ç –∫–æ–Ω—å, –æ—Ç–º–µ—Ç—å—Ç–µ –∞–Ω–≥–ª–∏–π—Å–∫–æ–π –±—É–∫–≤–æ–π N, –∞ –∫–ª–µ—Ç–∫–∏, –∫–æ—Ç–æ—Ä—ã–µ –±—å–µ—Ç –∫–æ–Ω—å, –æ—Ç–º–µ—Ç—å—Ç–µ —Å–∏–º–≤–æ–ª–∞–º–∏ *, –æ—Å—Ç–∞–ª—å–Ω—ã–µ –∫–ª–µ—Ç–∫–∏ –∑–∞–ø–æ–ª–Ω–∏—Ç–µ —Ç–æ—á–∫–∞–º–∏.
–§–æ—Ä–º–∞—Ç –≤—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
–ù–∞ –≤—Ö–æ–¥ –ø—Ä–æ–≥—Ä–∞–º–º–µ –ø–æ–¥–∞—é—Ç—Å—è –∫–æ–æ—Ä–¥–∏–Ω–∞—Ç—ã –∫–æ–Ω—è –Ω–∞ —à–∞—Ö–º–∞—Ç–Ω–æ–π –¥–æ—Å–∫–µ –≤ —à–∞—Ö–º–∞—Ç–Ω–æ–π –Ω–æ—Ç–∞—Ü–∏–∏ (—Ç–æ –µ—Å—Ç—å –≤ –≤–∏–¥–µ e4, –≥–¥–µ —Å–Ω–∞—á–∞–ª–∞ –∑–∞–ø–∏—Å—ã–≤–∞–µ—Ç—Å—è –Ω–æ–º–µ—Ä —Å—Ç–æ–ª–±—Ü–∞ 
(–±—É–∫–≤–∞ –æ—Ç a –¥–æ h, —Å–ª–µ–≤–∞ –Ω–∞–ø—Ä–∞–≤–æ), –∑–∞—Ç–µ–º –Ω–æ–º–µ—Ä—É —Å—Ç—Ä–æ–∫–∏ (—Ü–∏—Ñ—Ä–∞ –æ—Ç 1 –¥–æ 8, —Å–Ω–∏–∑—É –≤–≤–µ—Ä—Ö)).
–§–æ—Ä–º–∞—Ç –≤—ã—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
–ü—Ä–æ–≥—Ä–∞–º–º–∞ –¥–æ–ª–∂–Ω–∞ –≤—ã–≤–µ—Å—Ç–∏ –Ω–∞ —ç–∫—Ä–∞–Ω –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏–µ –¥–æ—Å–∫–∏, —Ä–∞–∑–¥–µ–ª—è—è —ç–ª–µ–º–µ–Ω—Ç—ã –ø—Ä–æ–±–µ–ª–∞–º–∏.
"""
s = input()  # "e4"
col_letter = s[0]                # 'e'
row_digit = int(s[1])            # 4

chess_col = 'abcdefgh'
col_index = chess_col.find(col_letter)  # 4
row_index = 8 - row_digit               # 4

chess_desk = [['.' for _ in range(8)] for _ in range(8)]

chess_desk[row_index][col_index] = "N"          #–ø–æ–∑–∏—Ü–∏—è –∫–æ–Ω—è


row_changes = [-2, -2, -1, -1, 1, 1, 2, 2]      # –≤–∞—Ä–∏–∞–Ω—Ç—ã —Ö–æ–¥–æ–≤ –∫–æ–Ω—è
col_changes = [-1, 1, -2, 2, -2, 2, -1, 1]

for i in range(8):  # 8 –≤–æ–∑–º–æ–∂–Ω—ã—Ö —Ö–æ–¥–æ–≤
    dr = row_changes[i]  # –∏–∑–º–µ–Ω–µ–Ω–∏–µ —Å—Ç—Ä–æ–∫–∏
    dc = col_changes[i]  # –∏–∑–º–µ–Ω–µ–Ω–∏–µ —Å—Ç–æ–ª–±—Ü–∞
    
    new_row = row_index + dr
    new_col = col_index + dc
    
    # –ü—Ä–æ–≤–µ—Ä—è–µ–º, —á—Ç–æ –∫–ª–µ—Ç–∫–∞ –≤ –ø—Ä–µ–¥–µ–ª–∞—Ö –¥–æ—Å–∫–∏
    if 0 <= new_row < 8 and 0 <= new_col < 8:
        chess_desk[new_row][new_col] = '*'

for i in chess_desk:
    print(*i)
