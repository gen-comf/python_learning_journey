# ===== ЗАДАЧА 3 работа со списками 3=====
"""
Вам доступен кортеж numbers. Напишите программу, которая выводит список, содержащий средние арифметические 
значения чисел каждого вложенного кортежа в заданном кортеже кортежей numbers.
""" 
numbers = ((10, 10, 10, 12), (30, 45, 56, 45), (81, 80, 39, 32), (1, 2, 3, 4), (90, 10))
rez = []
for i in numbers:
    x = sum(i) / len(i)
    rez.append(x)

print(rez)


