# ===== ЗАДАЧА 5 Конкурсный отбор=====
"""
Напишите программу, которая выводит список хорошистов и отличников в классе.
Формат входных данных
На вход программе подается натуральное число n, далее следует n строк с фамилией школьника и его оценкой на каждой из них.
Формат выходных данных
Программа должна вывести сначала все введённые строки с фамилиями и оценками учеников в том же порядке. 
Затем следует пустая строка, а затем выводятся строки с фамилиями и оценками хорошистов и отличников (в том же порядке).
""" 
n = int(input())

journal = [tuple(input().split()) for i in range(n) ]

[print(*journal[i]) for i in range(n)]

[print(*journal[i]) for i in range(n) if int(journal[i][1]) > 3]

# ===== ЗАДАЧА 6 Последовательность Трибоначчи=====
"""
Напишите программу, которая считывает натуральное число n и выводит первые n чисел последовательности Трибоначчи.
Формат входных данных
На вход программе подается натуральное число n(n≤100) – количество членов последовательности.
Формат выходных данных
Программа должна вывести члены последовательности Трибоначчи, отделенные символом пробела.
""" 

n = int(input())
f1, f2, f3 = 1, 1, 1
for i in range(n):
    print(f1, end = ' ')
    f1, f2, f3 = f2, f3, f1 + f2 + f3